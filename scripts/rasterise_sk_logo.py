def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--source",
        default=str(_ROOT / "SNOWKAP_logo.png"),
        help="Source PNG (RGBA preferred). Defaults to SNOWKAP_logo.png at repo root.",
    )
    args = parser.parse_args(argv)

    src = Path(args.source)
    if not src.exists():
        print(f"error: source PNG not found: {src}", file=sys.stderr)
        return 2

    try:
        from PIL import Image
    except ImportError:
        print("error: Pillow not installed. Run `pip install Pillow`.", file=sys.stderr)
        return 3

    im = Image.open(src).convert("RGBA")
    print(f"source: {src} ({im.size[0]}x{im.size[1]}, {im.mode})")

    out_dir = _ROOT / "client" / "public" / "assets"
    out_dir.mkdir(parents=True, exist_ok=True)

    _composite_and_save(im, 480, out_dir / "snowkap-logo-email.png", on_dark=True)
    _composite_and_save(im, 560, out_dir / "snowkap-logo.png", on_dark=True)
    _composite_and_save(im, 336, out_dir / "snowkap-logo-small.png", on_dark=True)
    _composite_and_save(im, 480, out_dir / "snowkap-logo-transparent.png", on_dark=False)

    _regenerate_email_assets_module(
        out_dir / "snowkap-logo-email.png",
        _ROOT / "engine" / "output" / "email_assets.py",
    )

    print("done. Commit the PNGs + email_assets.py and the next email send uses them.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
