"""Phase 11C+ — Regenerate the email logo assets from SNOWKAP_logo.png.

Source of truth for Phase 11+ is `SNOWKAP_logo.png` at the repo root (a
hand-exported PNG from Illustrator, since the PyMuPDF PDF path produced
letterforms with visible fringing). Run this whenever the brand mark
changes, then commit the regenerated `client/public/assets/*.png` and
`engine/output/email_assets.py`.

Emits:
    client/public/assets/snowkap-logo-email.png      (480x76 @ 2x retina, dark bg) → CID source
    client/public/assets/snowkap-logo.png            (560x~89, dark bg)
    client/public/assets/snowkap-logo-small.png      (336x~53, dark bg)
    client/public/assets/snowkap-logo-transparent.png (480x~76, no bg)
    engine/output/email_assets.py                    (regenerated base64)

Run:
    python scripts/rasterise_sk_logo.py

The old PyMuPDF (fitz) / cairo-based PDF path was removed in Phase 11C
because the hand-exported PNG preserves anti-aliasing on the SNOWKAP 'A'
and 'P' terminals that the PDF renderer was fringing.
"""

from __future__ import annotations

import argparse
import base64
import sys
import textwrap
from pathlib import Path

_ROOT = Path(__file__).resolve().parent.parent
BG_DARK = (15, 23, 42)  # #0F172A — matches BRAND_DARK in newsletter_renderer


def _composite_and_save(im, target_width: int, dest: Path, on_dark: bool) -> None:
    from PIL import Image

    w, _h = im.size
    scale = target_width / w
    new_size = (target_width, int(round(im.size[1] * scale)))
    resized = im.resize(new_size, Image.LANCZOS)
    if on_dark:
        canvas = Image.new("RGB", new_size, BG_DARK)
        canvas.paste(resized, mask=resized.split()[3])
        canvas.save(dest, format="PNG", optimize=True)
    else:
        resized.save(dest, format="PNG", optimize=True)
    print(f"  wrote {dest} ({dest.stat().st_size:,} bytes) {new_size[0]}x{new_size[1]}")


