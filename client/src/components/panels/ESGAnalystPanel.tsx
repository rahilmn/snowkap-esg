/** ESG Analyst perspective panel (Phase 4 Stage 11a output renderer).
 *
 * Renders the rich analyst-grade output: KPI table with peer quartiles,
 * confidence bounds on every ₹ estimate, double materiality split, TCFD
 * scenarios, SDG sub-goals, audit trail, framework citations.
 *
 * Pair with the existing KnowMoreSheet or mount stand-alone as a tab
 * inside ArticleDetailSheet. All fields degrade gracefully — missing
 * blocks are omitted (no empty placeholders).
 */

import type {
  EsgAnalystConfidenceBound,
  EsgAnalystPerspective,
  EsgAnalystFrameworkCitation,
  EsgAnalystKpi,
  EsgAnalystSdgTarget,
} from "@/types/perspectives";

interface ESGAnalystPanelProps {
  data: EsgAnalystPerspective;
  companyName?: string;
}

export function ESGAnalystPanel({ data, companyName }: ESGAnalystPanelProps) {
  return (
    <div className="space-y-6 text-sm text-gray-800">
      {/* Headline */}
      <header className="border-l-4 border-indigo-600 pl-4">
        <h2 className="text-lg font-semibold text-gray-900">{data.headline}</h2>
        {companyName && (
          <p className="text-xs text-gray-500 mt-1">ESG Analyst view · {companyName}</p>
        )}
        {data.generated_by && (
          <p className="text-[11px] text-gray-400 mt-0.5">
            Generated by <code>{data.generated_by}</code>
          </p>
        )}
      </header>

      {/* KPI table */}
      {data.kpi_table.length > 0 && <KpiTable kpis={data.kpi_table} />}

      {/* Confidence bounds */}
      {data.confidence_bounds.length > 0 && (
        <ConfidenceBounds bounds={data.confidence_bounds} />
      )}

      {/* Double materiality */}
      {(data.double_materiality.financial_impact || data.double_materiality.impact_on_world) && (
        <DoubleMateriality dm={data.double_materiality} />
      )}

      {/* TCFD scenarios */}
      {Object.values(data.tcfd_scenarios).some(Boolean) && (
        <TcfdScenarios scenarios={data.tcfd_scenarios} />
      )}

      {/* SDG targets */}
      {data.sdg_targets.length > 0 && <SdgTargets targets={data.sdg_targets} />}

      {/* Framework citations */}
      {data.framework_citations.length > 0 && (
        <FrameworkCitations cites={data.framework_citations} />
      )}

      {/* Audit trail */}
      {data.audit_trail.length > 0 && (
        <details className="bg-gray-50 border border-gray-200 rounded p-3">
          <summary className="cursor-pointer text-xs font-semibold text-gray-700">
            Audit trail ({data.audit_trail.length})
          </summary>
          <ul className="mt-2 space-y-2">
            {data.audit_trail.map((entry, i) => (
              <li key={i} className="text-xs">
                <div className="font-medium text-gray-800">{entry.claim}</div>
                <div className="text-gray-600 mt-0.5">{entry.derivation}</div>
                {entry.sources?.length > 0 && (
                  <div className="text-gray-400 mt-0.5">
                    sources: {entry.sources.join(", ")}
                  </div>
                )}
              </li>
            ))}
          </ul>
        </details>
      )}

      {/* Warnings (non-fatal, informational) */}
      {data.warnings.length > 0 && (
        <div className="text-[11px] text-amber-600 border-l-2 border-amber-300 pl-2">
          {data.warnings.map((w, i) => (
            <div key={i}>⚠ {w}</div>
          ))}
        </div>
      )}
    </div>
  );
}

